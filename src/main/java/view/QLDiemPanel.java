    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaMon, sb, "Phải nhập mã môn học !!! ");
        DataValidator.validateEmpty(txtDiem, sb, "Phải nhập điểm số !!! ");
        DataValidator.validateScore(txtDiem, sb, "Điểm nhập sai định dạng !!");

        if(sb.length() > 0){
            MessageDialogHelper.showErrorDialog(parentFrame,sb.toString(), "Error");
            return;
        }
        try {
            KQMonHoc result = new KQMonHoc();
            result.setMaMon(txtMaMon.getText());
            result.setMaSinhVien(txtMaSinhVienSearch.getText());
            result.setDiemSo(Float.parseFloat(txtDiem.getText()));
            result.setDiemChu(xepLoai(Float.parseFloat(txtDiem.getText())));
            result.setNamHoc(cbxNamHoc.getSelectedItem().toString());
            result.setHocKy(Integer.parseInt(cbxHocKy.getSelectedItem().toString()));

            KQMonHocDao resultDao = new KQMonHocDao();
            KQMonHoc kqmh = resultDao.find(result.getMaSinhVien(), result.getMaMon(), result.getNamHoc(), result.getHocKy());
            if( kqmh == null){
                if(resultDao.insert(result))
                    MessageDialogHelper.showMessageDialog(parentFrame, "Điểm đã được thêm", "Thành công");
                else    
                    MessageDialogHelper.showMessageDialog(parentFrame, "Điểm chưa được thêm", "Lỗi");            
            }
            else{
                if(resultDao.update(result, kqmh.getMaKQ()))
                    MessageDialogHelper.showMessageDialog(parentFrame, "Điểm đã được cập nhật", "Thành công");
                else    
                    MessageDialogHelper.showMessageDialog(parentFrame, "Điểm chưa được cập nhật", "Lỗi");
            }

            loadDataToTable();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showMessageDialog(parentFrame, e.getMessage(), "Error");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if(txtMaSinhVienSearch.getText().equals("")){
                MessageDialogHelper.showErrorDialog(parentFrame, "Mã sinh viên không được trống", "Lỗi");
            }else{
                KQMonHocDao resultDao = new KQMonHocDao();
                KQMonHoc result = resultDao.find(txtMaSinhVienSearch.getText(), txtMaMon.getText(),cbxNamHoc.getSelectedItem().toString(), Integer.parseInt(cbxHocKy.getSelectedItem().toString()) );
                if(result == null){
                    MessageDialogHelper.showMessageDialog(parentFrame, "Không tìm thấy kết quả cần xóa", "Error");
                    return;
                }
                if(MessageDialogHelper.showConfirmDialog(parentFrame, "Bạn chắc chắn muốn xóa", "Xác nhận") == JOptionPane.NO_OPTION)
                return;
                if(resultDao.delete(result))
                    MessageDialogHelper.showMessageDialog(parentFrame, "Xóa thành công", "Thành công");
                else
                    MessageDialogHelper.showMessageDialog(parentFrame, "Không xóa được", "Thất bại");
            }
            loadDataToTable();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showMessageDialog(parentFrame, e.getMessage(), "Error");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

