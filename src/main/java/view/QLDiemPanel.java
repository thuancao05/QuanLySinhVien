    private void loadDataToTable() throws SQLException {
        try {
            KQMonHocDao kqMonHocDao = new KQMonHocDao();
            MonHocDao monHocDao = new MonHocDao();
            
            defaultTableModel.setRowCount(0);

            try{
                List<KQMonHoc> list = kqMonHocDao.findAllKQMonHoc(txtMaSinhVienSearch.getText(), cbxNamHoc.getSelectedItem().toString(), Integer.parseInt(cbxHocKy.getSelectedItem().toString()));            
                float GPA = 0, soTinChi = 0;
                
                for(KQMonHoc kqMonHoc : list){
                    MonHoc monHoc = monHocDao.findByMaMon(kqMonHoc.getMaMon());
                    defaultTableModel.addRow(new Object[]{
                    monHoc.getMaMon(), monHoc.getTenMon(), monHoc.getSoTinChi(), kqMonHoc.getDiemSo(), kqMonHoc.getDiemChu()
                    });
                    GPA+= kqMonHoc.getDiemSo()*monHoc.getSoTinChi();
                    soTinChi+=monHoc.getSoTinChi();
                }
                GPA = GPA/soTinChi;
                if(GPA < 9 && GPA >= 8)
                    lblXepLoai.setText("GIỎI");
                else if(GPA < 8&& GPA >= 6.5)
                    lblXepLoai.setText("KHÁ");
                else if(GPA < 6.5&& GPA >= 5)
                    lblXepLoai.setText("TRUNG BÌNH");
                else if(GPA < 5 && GPA >= 4)
                    lblXepLoai.setText("YẾU");
                else if(GPA < 4)
                    lblXepLoai.setText("KÉM");
                else if(GPA >= 9)
                    lblXepLoai.setText("XUẤT SẮC");
                else
                    lblXepLoai.setText("NULL");
                lblGPA.setText( String.format("%.2f",GPA));
            }catch(Exception e){
                e.printStackTrace();
            }

            defaultTableModel.fireTableDataChanged();
        }
        
        catch(Exception e){
            e.printStackTrace();
            MessageDialogHelper.showMessageDialog(parentFrame, e.getMessage(), "Error");
        }
}

