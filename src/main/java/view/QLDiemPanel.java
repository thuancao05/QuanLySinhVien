        for(int i = 1; i <= 85; i++){
            bangDiem+="-";
        }
        bangDiem+="\n";
            
        try {
            FileWriter fw = new FileWriter(f,StandardCharsets.UTF_8);
            int n = tblGPA.getRowCount();
            int m = tblGPA.getColumnCount();
            for(int i = 0; i < n ; i++){
                for(int j = 0; j < m; j++){
                    bangDiem+= tblGPA.getValueAt(i, j) +"\t";
                    if(tblGPA.getValueAt(i, j).toString().length() < 15)
                        bangDiem+="\t";
                    if(j==1)
                        if(tblGPA.getValueAt(i, 1).toString().length() < 25)
                            bangDiem+="\t";
                }
                bangDiem+="\n";
            }
            for(int i = 1; i <= 85; i++){
                bangDiem+="-";
             }
            bangDiem+="\n";
            bangDiem+="Điểm trung bình học kỳ: " + lblGPA.getText() + " - Xếp loại: " + lblXepLoai.getText();
            
            System.out.println(bangDiem);

            fw.write(bangDiem);
            fw.close();
            
        } catch (IOException ex) {
            Logger.getLogger(QLDiemPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
