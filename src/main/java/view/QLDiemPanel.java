    private void tblGPAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGPAMouseClicked
        try {
            int row = tblGPA.getSelectedRow();
            if(row >= 0 ){
                
                String maSinhVien =  txtMaSinhVienSearch.getText(); 
                String maMon = (String) tblGPA.getValueAt(row, 0);
                float diem =  Float.parseFloat(tblGPA.getValueAt(row, 3).toString());
                String namHoc =  cbxNamHoc.getSelectedItem().toString();
                int hocKy = Integer.parseInt(cbxHocKy.getSelectedItem().toString());
                
                KQMonHocDao kqMonHocDao =  new KQMonHocDao();
                KQMonHoc kqMocHoc = kqMonHocDao.find(maSinhVien, maMon, namHoc, hocKy);
                SinhVienDao svDao = new SinhVienDao();
                SinhVien sv = svDao.findByID(maSinhVien);

                if(kqMocHoc != null){
                    
                    txtHoTen.setText(sv.getHoTen());
                    txtMaMon.setText(maMon);
                    xacDinhMonHoc();
                    txtDiem.setText(String.valueOf(diem));
                    lblDiemChu.setText(xepLoai(diem));
                                        
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentFrame, e.getMessage(), "Error");

        }
    }//GEN-LAST:event_tblGPAMouseClicked

