    public String xepLoai(float diem){
        String kq = new String();
        kq = "A";
        if(diem < 9 && diem >= 8)
           kq = "B+";
        else if(diem < 8&& diem >= 7)
           kq = "B";
        else if(diem < 7&& diem >= 6.5)
           kq = "C+";
        else if(diem < 6.5&& diem >= 5.5)
           kq = "C";
        else if(diem < 5.5&& diem >= 5)
           kq = "D+";
        else if(diem < 5 && diem >= 4)
           kq = "D";
        else if(diem < 4)
           kq = "F";
        return kq;
    }
    
